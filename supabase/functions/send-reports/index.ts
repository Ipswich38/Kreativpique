async function sendEmail(to: string[], subject: string, html: string) {
  const resendApiKey = Deno.env.get('RESEND_API_KEY')
  if (!resendApiKey) {
    throw new Error('Resend API key not configured')
  }

  const response = await fetch('https://api.resend.com/emails', {
    method: 'POST',
    headers: {
      'Authorization': `Bearer ${resendApiKey}`,
      'Content-Type': 'application/json',
    },
    body: JSON.stringify({
      from: 'kreativpique <reports@kreativpique.com>',
      to: to,
      subject: subject,
      html: html,
    }),
  })

  if (!response.ok) {
    const error = await response.text()
    throw new Error(`Failed to send email: ${error}`)
  }

  return await response.json()
}

serve(async (req) => {
  if (req.method === 'OPTIONS') {
    return new Response('ok', { headers: corsHeaders })
  }

  try {
    const supabase = createClient(
      Deno.env.get('SUPABASE_URL') ?? '',
      Deno.env.get('SUPABASE_SERVICE_ROLE_KEY') ?? '',
      {
        auth: {
          autoRefreshToken: false,
          persistSession: false
        }
      }
    )

    // Get the current date for the report period
    const now = new Date()
    const firstDayOfMonth = new Date(now.getFullYear(), now.getMonth(), 1)
    const lastDayOfMonth = new Date(now.getFullYear(), now.getMonth() + 1, 0)

    const reportPeriod = `${firstDayOfMonth.toLocaleDateString('en-US', {
      month: 'long',
      year: 'numeric'
    })}`

    // Get all active agencies
    const { data: agencies, error: agenciesError } = await supabase
      .from('agencies')
      .select(`
        *,
        users!inner(
          id,
          email,
          full_name,
          role
        )
      `)
      .eq('users.role', 'owner')

    if (agenciesError) {
      throw new Error(`Failed to fetch agencies: ${agenciesError.message}`)
    }

    const reports = []

    // Generate reports for each agency
    for (const agency of agencies || []) {
      // Get agency clients
      const { data: clients } = await supabase
        .from('clients')
        .select('*')
        .eq('agency_id', agency.id)
        .eq('is_active', true)

      if (!clients || clients.length === 0) continue

      // For each client, generate a report
      for (const client of clients) {
        // Get citations for this month
        const { data: citations } = await supabase
          .from('citations')
          .select('*')
          .eq('client_id', client.id)
          .gte('detected_at', firstDayOfMonth.toISOString())
          .lte('detected_at', lastDayOfMonth.toISOString())

        if (!citations || citations.length === 0) continue

        // Calculate metrics
        const totalCitations = citations.length

        // Average position
        const positionsWithValues = citations.filter(c => c.position !== null)
        const avgPosition = positionsWithValues.length > 0
          ? positionsWithValues.reduce((sum, c) => sum + (c.position || 0), 0) / positionsWithValues.length
          : 0

        // Top platform
        const platformCounts = new Map<string, number>()
        citations.forEach(c => {
          platformCounts.set(c.platform, (platformCounts.get(c.platform) || 0) + 1)
        })
        const topPlatform = Array.from(platformCounts.entries())
          .sort(([,a], [,b]) => b - a)[0]?.[0] || ''

        // Sentiment breakdown
        const sentimentBreakdown = {
          positive: citations.filter(c => c.sentiment === 'positive').length,
          neutral: citations.filter(c => c.sentiment === 'neutral').length,
          negative: citations.filter(c => c.sentiment === 'negative').length
        }

        // Top queries
        const queryCounts = new Map<string, { count: number; positions: number[] }>()
        citations.forEach(c => {
          const existing = queryCounts.get(c.query) || { count: 0, positions: [] }
          existing.count++
          if (c.position !== null) existing.positions.push(c.position)
          queryCounts.set(c.query, existing)
        })

        const topQueries = Array.from(queryCounts.entries())
          .map(([query, { count, positions }]) => ({
            query,
            citations: count,
            avgPosition: positions.length > 0
              ? Number((positions.reduce((sum, p) => sum + p, 0) / positions.length).toFixed(1))
              : 0
          }))
          .sort((a, b) => b.citations - a.citations)
          .slice(0, 5)

        // Prepare report data
        const reportData: CitationReportData = {
          clientName: client.name,
          agencyName: agency.name,
          reportPeriod,
          totalCitations,
          avgPosition: Number(avgPosition.toFixed(1)),
          topPlatform,
          sentimentBreakdown,
          topQueries,
          dashboardUrl: `${Deno.env.get('APP_URL') || 'https://app.kreativpique.com'}/clients/${client.id}`
        }

        // Generate HTML
        const html = await generateCitationReport('', reportData)

        // Send to agency owner and client contact (if provided)
        const recipients = [agency.users.email]
        if (client.contact_email) {
          recipients.push(client.contact_email)
        }

        // Send email
        const emailResult = await sendEmail(
          recipients,
          `AI Citation Report: ${client.name} - ${reportPeriod}`,
          html
        )

        reports.push({
          client: client.name,
          agency: agency.name,
          recipients: recipients.length,
          emailId: emailResult.id
        })

        console.log(`âœ… Report sent for ${client.name} (${agency.name})`)

        // Add delay to respect rate limits
        await new Promise(resolve => setTimeout(resolve, 1000))
      }
    }

    return new Response(
      JSON.stringify({
        success: true,
        message: `Sent ${reports.length} monthly reports`,
        reports
      }),
      {
        headers: { ...corsHeaders, 'Content-Type': 'application/json' },
        status: 200,
      }
    )

  } catch (error) {
    console.error('Error sending reports:', error)
    return new Response(
      JSON.stringify({
        success: false,
        error: error.message
      }),
      {
        headers: { ...corsHeaders, 'Content-Type': 'application/json' },
        status: 500,
      }
    )
  }
})